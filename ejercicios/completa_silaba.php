<script>
  function verificarRespuestas() {
    // Selecciona todos los elementos con la clase "correcta"
    var opcionesCorrectas = document.querySelectorAll('.correcta');

    // Inicializa un contador para las respuestas correctas
    var respuestasCorrectas = 0;

    // Recorre las opciones y cuenta las respuestas correctas
    for (var i = 0; i < opcionesCorrectas.length; i++) {
        if (opcionesCorrectas[i].checked) {
            respuestasCorrectas++;
        } else {
                // Si la opción no está seleccionada, muestra la "X"
                var contenedorRespuestaIncorrecta = opcionesCorrectas[i].closest('.card-body').querySelector('.respuesta-incorrecta');
                contenedorRespuestaIncorrecta.style.display = 'inline'; // Muestra la "X"
        }
    }
    

    Swal.fire({
        title: 'Respuestas correctas',
        text: 'Número de respuestas correctas: ' + respuestasCorrectas + " de: 6",
        confirmButtonText: 'Aceptar'
    });
} 
</script>